    public ReciboCredito(java.awt.Frame parent, boolean modal, ArrayList<Recibo> reci1) {
        super(parent, modal);
        initComponents();
        this.setLocationRelativeTo(null);
        df = new DecimalFormat("0.00");
        reci = reci1;
        com = new Dao_CompraProducto();
        if (reci != null) {
            for (Recibo r : reci) {
                for (CompraProducto c : com.listarprecio(r.getNomproducto())) {
                    costo.setText("" + df.format((c.getPrecio() + (c.getPrecio() * (c.getPorcentajeganancia() / 100))) / r.getTotales()));
                    precio.setText("" + df.format(c.getPrecio() + (c.getPrecio() * (c.getPorcentajeganancia() / 100))));
                }
                nombre.setText(r.getNombrec());
                ventadd.setText(r.getNumerofactura());
                numero.setText(r.getNumeroRecibo());
                nproducto.setText(r.getNomproducto());
                nfecha.setText(r.getFechapago());
                tiempo.setText(df.format(Double.parseDouble(r.getConto())) + " Meses.");
                restante.setText(r.getPagoTotal());
            }
        }
    }

