 @Override
    public int print(Graphics graf, PageFormat pagfor, int index) throws PrinterException {
        if (index > 0) {
            return NO_SUCH_PAGE;
        }
        Graphics2D hub = (Graphics2D) graf;
        hub.translate(pagfor.getImageableX() + 40, pagfor.getImageableY() + 40);
        hub.scale(1.0, 1.0);
        jPanel1.paintAll(graf);
        return PAGE_EXISTS;
    }
