    private void saveEMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveEMouseClicked

         if (nombre.getText().isEmpty() || apellido.getText().isEmpty() || telefono.getText().isEmpty() || dui.getText().isEmpty() || nit.getText().isEmpty() || correo.getText().isEmpty()) {
            JOptionPaneWornin a = new JOptionPaneWornin(null, true, "Registro", "LLenar Campos");
            a.setVisible(true);
        } else {
            vdui.setDui(dui.getText());
            vtele.setTelefono(telefono.getText());
            vnit.setNit(nit.getText());
            if (!vdui.isCorrecto()) {
                validui.setText("Dato incorrecto");
                JOptionPaneInfo gg = new JOptionPaneInfo(null, true, "Formato", "00000000-0");
                gg.setVisible(true);
                dui.requestFocus();
            } else {
                if (!vtele.isCorrecto()) {
                    valitelefono.setText("Dato incorrecto");
                    JOptionPaneInfo dd = new JOptionPaneInfo(null, true, "Formato", "0000-0000");
                    dd.setVisible(true);
                    telefono.requestFocus();
                } else {
                    if (!vnit.isCorrecto()) {
                        valinit.setText("Dato incorrecto");
                        JOptionPaneInfo dd = new JOptionPaneInfo(null, true, "Formato", "0000-000000-000-0");
                        dd.setVisible(true);
                        nit.requestFocus();
                    } else {
                        if (usu.Recuparar(nombre.getText(), apellido.getText(), dui.getText(), nit.getText(), correo.getText(), telefono.getText()).equals("")) {
                            JOptionPaneError dd = new JOptionPaneError(null, true, "Error", "Datos Incorrectos");
                            dd.setVisible(true);
                            con.setText("Los Datos Ingresados son Incorrectos..");
                            nombre.setText("");
                            apellido.setText("");
                            telefono.setText("");
                            dui.setText("");
                            nit.setText("");
                            correo.setText("");
                            nombre.requestFocus();
                        } else {
                            contra.setText("Tu contrase√±a es:");
                            con.setText(usu.Recuparar(nombre.getText(), apellido.getText(), dui.getText(), nit.getText(), correo.getText(), telefono.getText()));
                            nombre.setText("");
                            apellido.setText("");
                            telefono.setText("");
                            dui.setText("");
                            nit.setText("");
                            correo.setText("");
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_saveEMouseClicked

